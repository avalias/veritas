//! THE FINAL-PRODUCT DEMO: a fraud game over the COMMITTED-FLOAT Qwen —
//! the model exactly as published (bf16, PPL 34.60, zero quantization) —
//! settled by the Sui Move contract.
//!
//! A faulty resolver flips one bit of one bf16 weight mid-judgment
//! (32.3M float micro-ops); the bisection isolates the exact corrupted
//! transition; `verify_step` re-executes that ONE FDOT/FOP in pure-integer
//! softfloat and convicts. The challenger's StepProof is emitted as a Move
//! vector so `sui move test` demonstrates the conviction on-chain.
//!
//!   cargo run -p game --release --bin fqwen_dispute -- [n_prompt] [n_gen]
//!
//! Two-level scheme (SPEC §7.4): no trace vectors — cursor machines pinned
//! at the monotone `lo`, clone-and-advance per midpoint query.

use compiler::fqwen::compile_fqwen;
use game::actors::Claim;
use game::chain::{MockChain, Params, Role, Status};
use qwen::config::QwenConfig;
use qwen::flayout::{fgenesis, FLayout, FMEM_DEPTH};
use qwen::fmodel::FModel;
use qwen::tensors::SafeTensors;
use std::time::Instant;
use vm::exec::Machine;
use vm::hash::Hash;
use vm::isa::Instr;
use vm::onestep::{build_step_proof, JudgeParams, ProgramTree, StepProof};

const DIR: &str = concat!(env!("CARGO_MANIFEST_DIR"), "/../models/qwen/artifacts");

#[derive(Clone, Copy)]
struct Fault {
    step: u64,
    addr: u64,
    bit: u8,
}

struct Party {
    fault: Option<Fault>,
    cursor: Machine,
    stepped: u64,
    cloned: u32,
}

impl Party {
    fn new(p: u8, prog: Vec<Instr>, image: &[u8], fault: Option<Fault>) -> Self {
        let mut m = Machine::new(FMEM_DEPTH, p, prog);
        for (i, page) in image.chunks_exact(vm::PAGE_SIZE).enumerate() {
            if page.iter().any(|&b| b != 0) {
                m.mem.set_page(i as u64, page.try_into().unwrap());
            }
        }
        Self { fault, cursor: m, stepped: 0, cloned: 0 }
    }

    fn advance(m: &mut Machine, fault: Option<Fault>, target: u64, stepped: &mut u64) {
        while m.regs.step < target && m.regs.halted == 0 {
            let s = m.regs.step;
            m.step().expect("not terminal");
            *stepped += 1;
            if let Some(f) = fault {
                if f.step == s {
                    let v = m.mem.read_u8(f.addr) ^ (1 << (f.bit % 8));
                    m.mem.write(f.addr, &[v]);
                }
            }
        }
    }

    fn root_at(&mut self, step: u64) -> Hash {
        assert!(step >= self.cursor.regs.step);
        self.cloned += 1;
        let mut c = self.cursor.clone();
        Self::advance(&mut c, self.fault, step, &mut self.stepped);
        c.state_root()
    }

    fn sync_lo(&mut self, lo: u64) {
        let f = self.fault;
        let stepped = &mut self.stepped;
        Self::advance(&mut self.cursor, f, lo, stepped);
    }

    fn terminal(&mut self, out_base: u64) -> (u64, Hash, Vec<u8>) {
        self.cloned += 1;
        let mut c = self.cursor.clone();
        Self::advance(&mut c, self.fault, u64::MAX, &mut self.stepped);
        (c.regs.step, c.state_root(), c.mem.read(out_base, 4).to_vec())
    }

    fn proof_at(&mut self, lo: u64, tree: &ProgramTree) -> StepProof {
        self.cloned += 1;
        let mut c = self.cursor.clone();
        Self::advance(&mut c, self.fault, lo, &mut self.stepped);
        build_step_proof(&c, tree)
    }
}

fn hexs(b: &[u8]) -> String {
    b.iter().map(|x| format!("{x:02x}")).collect()
}

fn emit_move_vector(judge: &JudgeParams, pre_root: &Hash, claimed: &Hash, proof: &StepProof) {
    let sibs =
        |s: &[Hash]| s.iter().map(|h| format!("x\"{}\"", hexs(h))).collect::<Vec<_>>().join(", ");
    let opening = |o: &Option<vm::onestep::PageOpening>| match o {
        None => "        x\"\", vector[],\n".to_string(),
        Some(p) => format!("        x\"{}\", vector[{}],\n", hexs(&p.page), sibs(&p.siblings)),
    };
    let mut s = String::new();
    s.push_str(
        "/// GENERATED by `cargo run -p game --bin fqwen_dispute` — a REAL fault in\n\
         /// the COMMITTED-FLOAT Qwen3-0.6B (the model as published, PPL 34.60, no\n\
         /// quantization): one flipped bf16 weight bit, isolated by bisection over\n\
         /// a 32.3M-float-op judgment, convicted by the on-chain one-step verifier\n\
         /// executing the disputed float micro-op in pure-integer Move softfloat.\n\
         #[test_only]\n\
         module dispute::fqwen_conviction;\n\n\
         use dispute::interp;\n\n\
         #[test]\n\
         fun committed_float_qwen_fault_convicted() {\n\
         \x20   let v = interp::verify_step(\n",
    );
    s.push_str(&format!("        &x\"{}\",\n", hexs(pre_root)));
    s.push_str(&format!("        &x\"{}\",\n", hexs(claimed)));
    s.push_str(&format!("        {}, {},\n", judge.d, judge.p));
    s.push_str(&format!("        &x\"{}\",\n", hexs(&judge.program_root)));
    s.push_str(&format!("        x\"{}\",\n", hexs(&proof.regs)));
    s.push_str(&format!("        x\"{}\",\n", hexs(&proof.mem_root)));
    s.push_str(&format!("        x\"{}\",\n", hexs(&proof.instr)));
    s.push_str(&format!("        vector[{}],\n", sibs(&proof.instr_siblings)));
    s.push_str(&opening(&proof.open_a));
    s.push_str(&opening(&proof.open_b));
    s.push_str(&opening(&proof.open_w));
    s.push_str("    );\n    assert!(v == 1, 0); // ChallengerWins — float fraud convicted\n}\n");
    let path = concat!(env!("CARGO_MANIFEST_DIR"), "/../dispute/tests/fqwen_conviction.move");
    std::fs::write(path, &s).expect("write fqwen conviction vector");
    println!("· wrote on-chain conviction vector → dispute/tests/fqwen_conviction.move ({} B)", s.len());
}

fn main() {
    let mut args = std::env::args().skip(1);
    let n_prompt: usize = args.next().and_then(|s| s.parse().ok()).unwrap_or(2);
    let n_gen: usize = args.next().and_then(|s| s.parse().ok()).unwrap_or(1);

    println!("· loading bf16 Qwen (as published)…");
    let cfg = QwenConfig::load(&format!("{DIR}/config.json"));
    let st = SafeTensors::load(&format!("{DIR}/model.safetensors"));
    let m = FModel::load(&cfg, &st, qwen::flayout::FMAX_SEQ);
    drop(st);
    let lay = FLayout::new(&cfg);
    let prompt: Vec<u32> = (0..n_prompt as u32).map(|i| 9707 + 13 * i).collect();
    let image = fgenesis(&lay, &m, &prompt);
    let c = compile_fqwen(&lay, &cfg, n_prompt, n_gen);
    let tree = ProgramTree::new(&c.program, c.p);
    let judge = JudgeParams { d: FMEM_DEPTH, p: c.p, program_root: tree.root() };
    println!(
        "  float program: {} instrs (p = {}), judgment = {} micro-ops",
        c.program.len(),
        c.p,
        c.total_steps + 1
    );

    // The fraud: one bit of one bf16 weight (layer-14 gate matrix).
    let fault = Fault {
        step: c.token_boundaries[0] + 1_234_567,
        addr: lay.layers[14].w_gate + 99_998, // even: inside a bf16 cell
        bit: 6,
    };
    println!("· fault: flip bit {} of bf16 weight @{} at step {}", fault.bit, fault.addr, fault.step);

    let t0 = Instant::now();
    let mut resolver = Party::new(c.p, c.program.clone(), &image, Some(fault));
    let mut challenger = Party::new(c.p, c.program.clone(), &image, None);

    println!("· resolver computes + asserts (faulted)…");
    let (n, root_n, output) = resolver.terminal(lay.tok);
    let genesis_root = challenger.cursor.state_root();
    let chain = MockChain::new(Params::default());
    let mut fact =
        chain.assert_fact(judge.clone(), genesis_root, lay.tok, Claim { n, root_n, output });

    println!("· challenger recomputes…");
    let (cn, c_root, _) = challenger.terminal(lay.tok);
    assert_eq!(cn, n, "static schedule ⇒ same step count");
    assert_ne!(c_root, root_n, "fault must reach the final root");
    chain.challenge(&mut fact).expect("challenge");
    println!("· bisecting over {} float micro-ops…", n);

    let mut rounds = 0;
    let winner = loop {
        let d = fact.dispute.as_ref().unwrap().clone();
        if d.hi - d.lo == 1 {
            println!("  ⚖ atomic at step {} → {} (fault was at {})", d.lo, d.hi, fault.step);
            assert_eq!(d.lo, fault.step, "bisection isolated the corrupted transition");
            let proof = challenger.proof_at(d.lo, &tree);
            break chain.submit_proof(&mut fact, &proof).expect("valid proof");
        }
        let mid = d.lo + (d.hi - d.lo) / 2;
        chain.post_mid(&mut fact, resolver.root_at(mid)).expect("post_mid");
        let agree = challenger.root_at(mid) == fact.dispute.as_ref().unwrap().pending_mid.unwrap();
        chain.respond(&mut fact, agree).expect("respond");
        rounds += 1;
        let d2 = fact.dispute.as_ref().unwrap();
        println!(
            "  round {:>2}: mid {:>9} — {} — [{}, {}]",
            rounds,
            mid,
            if agree { "AGREE   " } else { "DISAGREE" },
            d2.lo,
            d2.hi
        );
        let lo = d2.lo;
        resolver.sync_lo(lo);
        challenger.sync_lo(lo);
    };
    assert_eq!(winner, Role::Challenger);
    assert_eq!(fact.status, Status::Rejected);

    // The on-chain artifact: the atomic step's proof, Move-convictable.
    let atomic_lo = fault.step;
    let pre_root = challenger.root_at(atomic_lo);
    let claimed_hi = resolver.root_at(atomic_lo + 1);
    let proof = challenger.proof_at(atomic_lo, &tree);
    emit_move_vector(&judge, &pre_root, &claimed_hi, &proof);

    println!("== verdict ==");
    println!("winner: {winner:?} (resolver slashed {})", fact.resolver_delta);
    println!(
        "rounds: {rounds}; stepped {}M+{}M; cloned {}+{}; wall {:.1?} (model load excl.)",
        resolver.stepped / 1_000_000,
        challenger.stepped / 1_000_000,
        resolver.cloned,
        challenger.cloned,
        t0.elapsed()
    );
    println!("✓ FINAL PRODUCT: published-quality float Qwen fraud isolated + convicted");
}
